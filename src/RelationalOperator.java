public class RelationalOperator {
    public static void main(String[] args){
        int x=10;
        int y=20;
        boolean result = x==y;
        System.out.println("value of equal to operator: "+result);
        result= x!=y;
        System.out.println("value of not equal to operator: " +result);
        result= x>y;
        System.out.println("value of greater than operator: " +result);
        result= x>=y;
        System.out.println("value of greater than or equal to operator: " +result);
        result= x<y;
        System.out.println("value of less than operator: " +result);
        result= x<=y;
        System.out.println("value of less than or equal to operator: " +result);

    }
}
